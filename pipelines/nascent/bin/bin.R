# action:
#     import two nascent RNA bigwig files, one for each primary genome strand
#     as needed, liftover the input nascent RNA-seq data to PRIMARY_GENOME
#     reformat the data to create a genome-wide transcription map that is:
#         _unstranded_, i.e., values reflect transcription on both strands
#         binned to BIN_SIZE bp bins, for direct comparison to ATAC-seq data
# input:
#     two nascent RNA bigwig files for PRIMARY_GENOME
#     genome bins BED file for COMPOSITE_GENOME
#     optional liftover chain file from bigwig genome to PRIMARY_GENOME
# where, we generally assumme Pro-seq data are:
#     from: https://www.ncbi.nlm.nih.gov/geo/query/acc.cgi?acc=GSE228452 
#     and thus were: "generated by summing counts per nucleotide across replicates"
#     i.e., each strand file has one entry for every covered base, with its count of matching read 3' ends
# outputs:
#     transcription map as a composite-genome-wide bin BED file where
#       score = cpm over both strands
#       cpm is Counts Per Million reads == Reads Per Million == RPKM at bin size == 1kb
#       both primary and spike-in genome bins are included, but spike-in bins always have 0 cpm

#=====================================================================================
# script initialization
#-------------------------------------------------------------------------------------
message("initializing")
suppressPackageStartupMessages(suppressWarnings({
    library(rtracklayer)
    library(data.table)
    library(parallel)
}))
#-------------------------------------------------------------------------------------
# load, parse and save environment variables
env <- as.list(Sys.getenv())
rUtilDir <- file.path(env$MODULES_DIR, 'utilities', 'R')
source(file.path(rUtilDir, 'workflow.R'))
checkEnvVars(list(
    string = c(
        'PRIMARY_GENOME',
        'BIGWIG_FILE_FORWARD',
        'BIGWIG_FILE_REVERSE',
        'LIFTOVER_CHAIN',
        'GENOME_BINS_BED',
        'DATA_FILE_PREFIX'
    ),
    integer = c(
        'BIN_SIZE',
        'N_CPU'
    )
))
#-------------------------------------------------------------------------------------
# set some options
options(scipen = 999) # prevent 1+e6 in printed, which leads to read.table error when integer expected
options(warn = 2)
#=====================================================================================

#=====================================================================================
# do the work
#-------------------------------------------------------------------------------------
message("importing (and lifting over) bigwig files")
nascent <- do.call(rbind, mclapply(c(env$BIGWIG_FILE_FORWARD, env$BIGWIG_FILE_REVERSE), function(bw_file) {
# nascent <- do.call(rbind, lapply(c(env$BIGWIG_FILE_FORWARD, env$BIGWIG_FILE_REVERSE), function(bw_file) {

    message(paste(" ", "importing", bw_file))
    bw_data <- import(bw_file, format = "bigWig") # a GRanges object
    if(env$LIFTOVER_CHAIN != "NA") {
        message(paste(" ", "lifting with ", env$LIFTOVER_CHAIN))
        chain <- import.chain(env$LIFTOVER_CHAIN)
        bw_data <- unlist(liftOver(bw_data, chain))
    }

    # convert to data.table
    # width is always 1, strand is always "*", since F and R files are provided
    # chrom names in bigwig files expected to match PRIMARY_GENOME with chrom names like chr1, chrX
    dt <- as.data.table(bw_data) 
    rm(bw_data)
    dt[, .(chrom = as.character(seqnames), start0 = start - 1L, count = score)]
}, mc.cores = env$N_CPU))
# }))

message("creating unstranded transcription map by counting read 3' endpoints per bin")
nascent[, start0 := floor(start0 / env$BIN_SIZE) * env$BIN_SIZE] # the start0 of the bin containing the read 3' end
nascent <- nascent[, .(count = sum(count, na.rm = TRUE)), keyby = .(chrom, start0)] # sum the counts per bin

message("loading genome bins")
# GENOME_BINS_BED expected to match COMPOSITE_GENOME, with chrom names like chr1-mm39, chr2L-dm6
bins <- fread(env$GENOME_BINS_BED)

message("adjusting chrom names to match composite genome")
nascent[, chrom := paste0(chrom, "-", env$PRIMARY_GENOME)]

message("merging into genome bins, including empty bins")
nascent <- merge(
    bins[, .(chrom, start0, end1)], 
    nascent, 
    by = c('chrom', 'start0'), 
    all.x = TRUE,
    sort = FALSE
)
nascent[is.na(count), count := 0L]

message("coverting counts to CPM")
nascent[, cpm := count / sum(count, na.rm = TRUE) * 1e6]

message("writing nascent transcriptome output file")
nascent <- nascent[, .(chrom, start0, end1, cpm)]
fwrite(
    nascent, 
    file = paste(env$DATA_FILE_PREFIX, "nascent_transcriptome_unstranded.bed.gz", sep = '.'),
    quote = FALSE,
    row.names = FALSE,
    col.names = TRUE,
    sep = '\t'
)
message()
str(nascent)
